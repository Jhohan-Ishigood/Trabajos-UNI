# CONVERSION DE TEMPERATURA:
print("CONVERSIÓN DE TEMPERATURA:")

# Entrada de datos
grad_Celsius = float(input("Ingrese temperatura en grados Celsius: "))

# Conversión
grad_Fahrenheit = (grad_Celsius * 1.8) + 32

# Salida
print(f"{grad_Celsius} grados Celsius en Fahrenheit es: {grad_Fahrenheit}°")
